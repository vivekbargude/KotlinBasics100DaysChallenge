package Operators// Operators.kt

fun arithmeticOperators() {
    val a = 10
    val b = 3

    println("\n--- Arithmetic Operators ---")
    println("a + b = ${a + b}")
    println("a - b = ${a - b}")
    println("a * b = ${a * b}")
    println("a / b = ${a / b}")  // Integer division
    println("a % b = ${a % b}")  // Modulo (remainder)

    println("\n--- Comparison Operators ---")
    println("a == b: ${a == b}")   // Equality
    println("a != b: ${a != b}")   // Not equal
    println("a > b: ${a > b}")
    println("a < b: ${a < b}")
}
